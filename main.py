def verify_ip_geodata(ip):
    """
    å¼ºåˆ¶æ ¡éªŒï¼šå¹¿ä¸œçœã€ä¸­å±±å¸‚ã€ä¸­å›½ç”µä¿¡
    """
    try:
        # ip-api.com å…è´¹ç‰ˆé™åˆ¶ 45æ¬¡/åˆ†é’Ÿ
        url = f"http://ip-api.com/json/{ip}?lang=zh-CN"
        res = requests.get(url, timeout=8).json()
        
        if res.get("status") != "success":
            return False

        region = res.get("regionName", "")
        city = res.get("city", "")
        isp = res.get("isp", "")
        org = res.get("org", "")

        is_guangdong = "å¹¿ä¸œ" in region
        is_zhongshan = "ä¸­å±±" in city
        # æ¨¡ç³ŠåŒ¹é…ç”µä¿¡å…³é”®å­—
        isp_info = (isp + org).lower()
        is_telecom = any(kw in isp_info for kw in ["ç”µä¿¡", "telecom", "chinatelecom"])

        if is_guangdong and is_zhongshan and is_telecom:
            return True
    except Exception as e:
        print(f"   âš ï¸  IPæ ¡éªŒå¼‚å¸¸ ({ip}): {e}")
    return False

def check_stream(url, timeout=5):
    """ä½¿ç”¨ ffprobe æ£€æµ‹ä¿¡å·æœ‰æ•ˆæ€§"""
    try:
        cmd = ["ffprobe", "-v", "error", "-show_streams", "-i", url]
        result = subprocess.run(cmd, stdout=subprocess.PIPE, stderr=subprocess.PIPE, timeout=timeout+2)
        return b"codec_type" in result.stdout
    except:
        return False

